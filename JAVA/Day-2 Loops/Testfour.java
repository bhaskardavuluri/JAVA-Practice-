//For-loop example
public class Testfour {
    public static void main(String[] args) {
        for(int i=30;i>=1;i--){  //Reverse order 
            System.out.println(i);
            System.out.println("*************");
            for(int j=0;j<=30;j++){
                System.out.println(j); //ascending order 
            }
        }
    }
}
